/* Para cambiar el tipo de datos de las variables se utilizan las 
   funciones parseInt(), parseFloat(), String(), Number(), Boolean()*/

// cadena a entero

let num = "10"
let numInt = parseInt(num) //también se puede usar Number(num) y concatenarlo con un +num, es el mismo resultado
console.log(numInt);

// cadena a flotante

let numero = "9.81"
let numFloat = parseFloat(numero) //también se puede usar Number(numero) y concatenarlo con un +numero, es el mismo resultado
console.log(numFloat);

// Float a entero

let numFlotante = 9.81
let numEntero = parseInt(numFlotante)
console.log(numEntero);


